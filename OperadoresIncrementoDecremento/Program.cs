//pré-inclemento=primeiro resolve depois incrementa
int z = 0;
Console.WriteLine($"Z = {z}");
int resultadoz =++z + 10;
Console.WriteLine($"pós-inclemento ===> {resultadoz}");
Console.WriteLine($"valor de Z ===> {z}\n");

//pós-declemento=primeiro resolve depois incrementa
int u = 0;
Console.WriteLine($"U = {u}");
int resultadou = u-- + 10;
Console.WriteLine($"pós-inclemento ===> {resultadou}");
Console.WriteLine($"valor de t ===> {u}\n");


//pré-declemento=primeiro resolve depois incrementa
